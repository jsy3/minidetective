function buildThreeClueProblems(problems) {
  const threeClueProblems = problems.filter(
    (problem) => Array.isArray(problem.clues) && problem.clues.length === 3,
  );

  if (threeClueProblems.length === 0) {
    return [];
  }

  return problems.map((_, index) => {
    const source = threeClueProblems[index % threeClueProblems.length];
    return {
      ...source,
      id: String(index + 1),
      clues: [...source.clues],
    };
  });
}

export const MYSTERY_PROBLEMS = buildThreeClueProblems(RAW_MYSTERY_PROBLEMS);

export function getProblemByIndex(index) {
  return MYSTERY_PROBLEMS[index % MYSTERY_PROBLEMS.length] ?? MYSTERY_PROBLEMS[0];
}

export function getTotalCount() {
  return MYSTERY_PROBLEMS.length;
}

/** id 기준 랜덤 문제 반환 */
export function getRandomProblem() {
  const idx = Math.floor(Math.random() * MYSTERY_PROBLEMS.length);
  return MYSTERY_PROBLEMS[idx];
}
